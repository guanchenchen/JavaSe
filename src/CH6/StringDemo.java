package CH6;

public class StringDemo {
	public static void main(String[] args) {
		String text = "hello";

		System.out.println("字串热: " + text);
		System.out.println("字串L度: " + text.length());
		System.out.println("等於hello? " + text.equals("hello"));
		System.out.println("D榇: " + text.toUpperCase());
		System.out.println("D樾: " + text.toLowerCase());
	}

}
