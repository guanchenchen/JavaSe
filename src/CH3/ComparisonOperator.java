package CH3;

public class ComparisonOperator {
	public static void main(String[] args) {
		System.out.println("10 >  5 Y果 ." + (10 > 5));
		System.out.println("10 >= 5 Y果. " + (10 >= 5));
		System.out.println("10 <  5 Y果 ." + (10 < 5));
		System.out.println("10 <= 5 Y果." + (10 <= 5));
		System.out.println("10 == 5 Y果. " + (10 == 5));
		System.out.println("10 != 5 Y果. " + (10 != 5));
	}

}
